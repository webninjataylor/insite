                <h2>Project Manager:</h2>
                <p><strong>Responsibilities include but are not limited to:</strong><br/>
                    The qualified candidate must manage and direct assigned staff to meet scope, schedule and budgetary goals for a variety of projects. The candidate must supervise the preparation of reports, site and subdivision plans, specifications, and other contract documents, and review and approve these documents. The candidate must foster the use of innovative concepts in the development of project materials, and prepare more complex project materials. The candidate must assign tasks and monitor staff performance to ensure all schedules and budgets are maintained, and that project goals are met to the satisfaction of the client. The candidate must approve all project schedules, budgets and QA/QC plans. The candidate will be required to attend weekly internal meeting to discuss project progress and to assist in the preparation of proposals, contracts and presentations.</p>
                <p><strong>Experience required:</strong><br />
                    The successful candidate will possess, at minimum, a bachelor’s degree, and at least five (5) years of experience in Land Development Design, and must have experience with management of site plan and subdivision preparation and stormwater management design.  Candidates should have extensive technical knowledge and the ability to successfully manage both manpower and financial resources. Some field work and attendance at client functions may be required.</p>
                <a name="designengineer"></a>
                <h2 class="divider">Design Engineer:</h2>
                <p><strong>Responsibilities:</strong><br />
                    The qualified candidate must assist the Project Manager and/or Professional Engineer with the design of land development projects.  The candidate must gather data from pertinent regulations and available mapping to assist in preparing basemaps for each project.  The candidate must be able to prepare basic design criteria such as grading, construction details, zoning analyses, and must have a working knowledge of stormwater management designs.</p>
                <p><strong>Experience required:</strong><br />
                    The qualified candidate must have at least two (2) years experience working in the capacity described above.  The candidate must be proficient in AutoCAD version 2000 or greater, as well as Land Desktop software.  A working knowledge of HyrdoCAD and GIS is a plus.  The candidate must have an accredited bachelor’s degree in civil engineering, and must have EIT certification or be on track to receive same.  It is preferable that the candidate seek Professional Engineering licensure.</p>
                <a name="draftperson"></a>
                <h2 class="divider">Draftperson:</h2>
                <p><strong>Responsibilities:</strong><br/>
                    The qualified candidate must be proficient in AutoCAD version 2004 or greater, as well as Land Desktop software.  The candidate must assist the Project Manager and/or Professional Engineer in preparing plan sets for subdivisions, site plans, and plot plans.  Drafting work includes, but is not limited to: digital terrain modeling, cross sections, profiles, road templates, cut/fill calculations, construction details, key maps, raster images, external references, and layer management.  The candidate will be responsible to ensure consistency of plan appearance prior to submission to the Project Manager and/or Professional Engineer for review.  The candidate must develop a thorough knowledge of InSite’s CAD Standards manual.</p>
                <p><strong>Experience required:</strong><br />
                    The qualified candidate must have at least three (3) years of experience working in the capacity described above, as well as a high school diploma.  The candidate must be knowledgeable in general computer operation and maintenance, such as: networking, Microsoft Office, and plotters and printers.</p>
